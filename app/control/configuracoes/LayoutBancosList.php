    /**
     * Register the filter in the session
     */
    public function onSearch($param = null)
    {
        $data = $this->form->getData();
        $filters = [];

        TSession::setValue(__CLASS__.'_filter_data', NULL);
        TSession::setValue(__CLASS__.'_filters', NULL);

        if (isset($data->id) AND ( (is_scalar($data->id) AND $data->id !== '') OR (is_array($data->id) AND (!empty($data->id)) )) )
        {

            $filters[] = new TFilter('id', '=', $data->id);// create the filter 
        }

        if (isset($data->nome) AND ( (is_scalar($data->nome) AND $data->nome !== '') OR (is_array($data->nome) AND (!empty($data->nome)) )) )
        {

            $filters[] = new TFilter('nome', 'like', "%{$data->nome}%");// create the filter 
        }

        if (isset($data->bancos_modulos_id) AND ( (is_scalar($data->bancos_modulos_id) AND $data->bancos_modulos_id !== '') OR (is_array($data->bancos_modulos_id) AND (!empty($data->bancos_modulos_id)) )) )
        {

            $filters[] = new TFilter('bancos_modulos_id', '=', $data->bancos_modulos_id);// create the filter 
        }

        if (isset($data->logomarca) AND ( (is_scalar($data->logomarca) AND $data->logomarca !== '') OR (is_array($data->logomarca) AND (!empty($data->logomarca)) )) )
        {

            $filters[] = new TFilter('logomarca', 'like', "%{$data->logomarca}%");// create the filter 
        }

        if (isset($data->codigo_layout) AND ( (is_scalar($data->codigo_layout) AND $data->codigo_layout !== '') OR (is_array($data->codigo_layout) AND (!empty($data->codigo_layout)) )) )
        {

            $filters[] = new TFilter('codigo_layout', 'like', "%{$data->codigo_layout}%");// create the filter 
        }

        if (isset($data->tipo_layout) AND ( (is_scalar($data->tipo_layout) AND $data->tipo_layout !== '') OR (is_array($data->tipo_layout) AND (!empty($data->tipo_layout)) )) )
        {

            $filters[] = new TFilter('tipo_layout', 'like', "%{$data->tipo_layout}%");// create the filter 
        }

        // fill the form with data again
        $this->form->setData($data);

        // keep the search data in the session
        TSession::setValue(__CLASS__.'_filter_data', $data);
        TSession::setValue(__CLASS__.'_filters', $filters);

        $this->onReload(['offset' => 0, 'first_page' => 1]);
    }

    /**
     * Load the datagrid with data
     */
    public function onReload($param = NULL)
    {
        try
        {
            // open a transaction with database 'conecatarbanco'
            TTransaction::open(self::$database);

            // creates a repository for LayoutBancos
            $repository = new TRepository(self::$activeRecord);

            $criteria = clone $this->filter_criteria;

            if (empty($param['order']))
            {
                $param['order'] = 'id';    
            }

            if (empty($param['direction']))
            {
                $param['direction'] = 'desc';
            }

            $criteria->setProperties($param); // order, offset
            $criteria->setProperty('limit', $this->limit);

            if($filters = TSession::getValue(__CLASS__.'_filters'))
            {
                foreach ($filters as $filter) 
                {
                    $criteria->add($filter);       
                }
            }

            //</blockLine><btnShowCurtainFiltersAutoCode>
            if(!empty($this->btnShowCurtainFilters) && empty($this->btnShowCurtainFiltersAdjusted))
            {
                $this->btnShowCurtainFiltersAdjusted = true;
                $this->btnShowCurtainFilters->style = 'position: relative';
                $countFilters = count($filters ?? []);
                $this->btnShowCurtainFilters->setLabel($this->btnShowCurtainFilters->getLabel(). "<span class='badge badge-success' style='position: absolute'>{$countFilters}<span>");
            }
            //</blockLine></btnShowCurtainFiltersAutoCode>

            // load the objects according to criteria
            $objects = $repository->load($criteria, FALSE);

            $this->datagrid->clear();
            if ($objects)
            {
                // iterate the collection of active records
                foreach ($objects as $object)
                {

                    $row = $this->datagrid->addItem($object);
                    $row->id = "row_{$object->id}";

                }
            }

            // reset the criteria for record count
            $criteria->resetProperties();
            $count= $repository->count($criteria);

            $this->pageNavigation->setCount($count); // count of records
            $this->pageNavigation->setProperties($param); // order, page
            $this->pageNavigation->setLimit($this->limit); // limit

            // close the transaction
            TTransaction::close();
            $this->loaded = true;

            return $objects;
        }
        catch (Exception $e) // in case of exception
        {
            // shows the exception error message
            new TMessage('error', $e->getMessage());
            // undo all pending operations
            TTransaction::rollback();
        }
    }

